import pandas as pd
import os

# 确保目录存在
os.makedirs('data', exist_ok=True)
os.makedirs('config', exist_ok=True)

# 1. 创建 V1 版本 (基准数据)
data_v1 = {
    '员工ID': ['1001', '1002', '1003'],
    '姓名': ['张三', '李四', '王五'],
    '部门': ['技术部', '市场部', '财务部'],
    '工资': [10000, 15000, 12000],
    '入职日期': ['2023-01-10', '2023-03-15', '2023-05-20']
}
df_v1 = pd.DataFrame(data_v1)
df_v1.to_excel('data/input_v1.xlsx', index=False)

# 2. 创建 V2 版本 (包含变更和追加)
